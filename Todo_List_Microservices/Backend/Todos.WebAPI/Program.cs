app.MapGet("todos/create", (string work, ApplicationDbContext context) =>
{
    Todo todo = new()
    {
        Work = work,
    };
    context.Add(todo);
    context.SaveChanges();
    return new { Messages = "Todo create is successful" };
});

app.MapGet("todos/getall", (ApplicationDbContext context) =>
{
    var todos = context.Todos.ToList();
    return todos;
});

